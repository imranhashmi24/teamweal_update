     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $order = Order::with('service')->where('id',$id)->first();
        $order->seen = 1;
        $order->save();
        
        return view('admin.orders.show', compact('order'));
    }

